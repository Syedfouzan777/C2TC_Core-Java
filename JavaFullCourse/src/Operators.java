
public class Operators {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = ++a;
		int d = a++;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a++);
		System.out.println(b--);
		System.out.println(++a);
		System.out.println(--a);

	}

}
