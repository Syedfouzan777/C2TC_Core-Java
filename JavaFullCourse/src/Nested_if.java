
public class Nested_if {

	public static void main(String[] args) {
		String lang = "Python";
		if(lang == "c") {
			if(lang == "c++")
				System.out.println("Lang is c++");
			}else if(lang == "java") {
				System.out.println("Lang is java");
			}else if(lang == "Python") {
				System.out.println("Lang is python");
			}else {
				System.out.println("Null");
			}
		
	}

}
